def normalize_vitalhealth_product_name(name: str) -> str:
    key = (name or "").strip()
    if not key:
        return key
    return VITALHEALTH_ALIASES.get(key, key)


def get_vitalhealth_product(name: str) -> Optional[Dict[str, Any]]:
    normalized = normalize_vitalhealth_product_name(name)
    return VITALHEALTH_CATALOG.get(normalized)


def get_product_categories(product_name: str) -> List[Dict[str, Any]]:
    product = get_vitalhealth_product(product_name)
    return product.get("categories", []) if product else []


